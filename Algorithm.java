public class Algorithm {
    String name;
    String category;
    String timeComplexity;
    boolean supportsNegative;

    public Algorithm(String name, String category, String timeComplexity, boolean supportsNegative) {
        this.name = name;
        this.category = category;
        this.timeComplexity = timeComplexity;
        this.supportsNegative = supportsNegative;
    }
}
